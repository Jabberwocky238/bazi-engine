export function triadOf(z: Zhi): TriadKey {
  if ("申子辰".includes(z)) return "申子辰";
  if ("寅午戌".includes(z)) return "寅午戌";
  if ("亥卯未".includes(z)) return "亥卯未";
  return "巳酉丑";
}

